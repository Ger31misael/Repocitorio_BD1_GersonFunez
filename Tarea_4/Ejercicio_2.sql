USE Ejercicio_2;

CREATE TABLE DEPARTAMENTO(
    Cod_Departamento VARCHAR (10) PRIMARY KEY NOT NULL,
    Nombre_Departamento VARCHAR (50) 

);

CREATE TABLE MUNICIPIO(
    Cod_Municipio VARCHAR(10) PRIMARY KEY NOT NULL,
    Nombre_Municipio VARCHAR(50) NOT NULL,
    Cod_Departamento VARCHAR (10) FOREIGN KEY REFERENCES DEPARTAMENTO(Cod_Departamento)
);

CREATE TABLE LOCALIDAD(
    Cod_localidad VARCHAR(10) PRIMARY KEY NOT NULL,
    Nombre_localidad VARCHAR (50) NOT NULL,
    Cod_Municipio VARCHAR(10) FOREIGN KEY REFERENCES MUNICIPIO(Cod_Municipio)

);

CREATE TABLE CIUDADANO(
    Id VARCHAR(15) PRIMARY KEY NOT NULL,
    Nombre VARCHAR(50) NOT NULL,
    Salario VARCHAR(30) NOT NULL,
    Telefono VARCHAR(10) NOT NULL,
    Codigo_localidad VARCHAR (10) FOREIGN KEY REFERENCES LOCALIDAD(Cod_localidad)

);

