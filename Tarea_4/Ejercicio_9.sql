CREATE TABLE LIBRO(
    Titulo VARCHAR (50) PRIMARY KEY NOT NULL,
    Genero VARCHAR (100) NOT NULL,
    Reseña VARCHAR (100) NOT NULL,
    Texto VARCHAR (100) NOT NULL,
    Fecha DATE NOT NULL,
    Revista VARCHAR (100) NOT NULL
);

