CREATE TABLE ESCRIBE(
    Nombre_Autor VARCHAR (100) FOREIGN KEY REFERENCES AUTOR(Nombre_Autor),
    Titulo VARCHAR (50) FOREIGN KEY REFERENCES LIBRO(Titulo)
);

